import numpy as np
import pylab # –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è 2-—Ö –∏ 3-—Ö –º–µ—Ä–Ω—ã—Ö –º–∞—Å—Å–∏–≤–æ–≤
import scipy.stats as stats

