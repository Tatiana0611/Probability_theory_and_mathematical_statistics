# –ø—Ä–æ–≤–µ—Ä–∏—Ç—å —ç—Ç—É –≥–∏–ø–æ—Ç–µ–∑—É, –µ—Å–ª–∏ –≤ –≤—ã–±–æ—Ä–∫–µ –∏–∑ n=16 —à–∞—Ä–∏–∫–æ–≤, —Å—Ä–µ–¥–Ω–µ–µ –æ–∫–∞–∑–∞–ª–æ—Å—å —Ä–∞–≤–Ω—ã–º 10,3 –º–º, 
# –∞ –¥–∏—Å–ø–µ—Ä—Å–∏—è –Ω–∞–º –∏–∑–≤–µ—Å—Ç–Ω–∞ –∏ —Ä–∞–≤–Ω–∞ 1

# –§–æ—Ä–º—É–ª–∏—Ä–æ–≤–∞–Ω–∏–µ –≥–∏–ø–æ—Ç–µ–∑:
# H0: Œº = Œº0 –Ω—É–ª–µ–≤–∞—è –≥–∏–ø–æ—Ç–µ–∑–∞
# H1: Œº > 0 –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω–∞—è –≥–∏–ø–æ—Ç–µ–∑–∞, –∑–Ω–∞–∫ > —Ç.–∫. 10,3 –º–º > 10 –º–º

# –í—ã–±–æ—Ä –∫—Ä–∏—Ç–µ—Ä–∏—è
# ùëç–Ω=(ùëã‚Äæ ‚àí Œº)/(ùûº/‚àöùëõ)

# Œ±=0.05
# ùëç—Ç=1.65 —Ç–∞–±–ª–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –ø–æ –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏(–Ω–∞—Ö–æ–¥–∏–º –ø–æ —Ç–∞–±–ª–∏—Ü–µ Z=0,95, –µ–º—É —Å–æ–æ—Ç–≤–µ—Ç—Å–≤—É–µ—Ç X=1.65)
# ùëç–Ω=(ùëã‚Äæ ‚àí Œº)/(ùûº/‚àöùëõ)=(10.3‚àí10)/(1/‚àö16)=1.2 —Ä–∞—Å—á–µ—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –ø–æ –≤—ã–±–æ—Ä–∫–µ

# –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º Z–Ω –∏ Z—Ç
# –†–∞—Å—á–µ—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ ùëç–Ω –ø–æ–ø–∞–¥–∞–µ—Ç –≤ –æ–±–ª–∞—Å—Ç—å –ø—Ä–∏–Ω—è—Ç–∏—è –Ω—É–ª–µ–≤–æ–π –≥–∏–ø–æ—Ç–µ–∑—ã (Z–Ω < Z—Ç), –¥–µ–ª–∞–µ–º –≤—ã–≤–æ–¥, 
# —á—Ç–æ –Ω–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∏ –∑–Ω–∞—á–∏–º—ã—Ö —Ä–∞–∑–ª–∏—á–∏–π –º–µ–∂–¥—É —Å—Ä–µ–¥–Ω–∏–º, –ø–æ–ª—É—á–µ–Ω–Ω—ã–º –ø–æ –≤—ã–±–æ—Ä–∫–µ, –∏ —Å—Ä–µ–¥–Ω–∏–º, –ø–æ–ª—É—á–µ–Ω–Ω—ã–º –ø–æ –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏
# –í—ã–≤–æ–¥: —à–∞—Ä–∏–∫–∏ –¥–ª—è –ø–æ–¥—à–∏–ø–Ω–∏–∫–æ–≤ –∏–º–µ—é—Ç –¥–∏–∞–º–µ—Ç—Ä 10 –º–º . –ì–∏–ø–æ—Ç–µ–∑–∞ –≤–µ—Ä–Ω–∞ –ø—Ä–∏ ùõº=0.05

