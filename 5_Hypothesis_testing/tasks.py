# –ù–∏–∂–µ –ø—Ä–∏–≤–µ–¥–µ–Ω—ã –¥–∏–∞–º–µ—Ç—Ä—ã –∫–æ—Ä–æ–Ω–∞—Ä–Ω—ã—Ö –∞—Ä—Ç–µ—Ä–∏–π –ø–æ—Å–ª–µ –ø—Ä–∏–µ–º–∞ –Ω–∏—Ñ–µ–¥–∏–ø–∏–Ω–∞ –∏ –ø–ª–∞—Ü–µ–±–æ. –ü–æ–∑–≤–æ–ª—è—é—Ç –ª–∏ –ø—Ä–∏–≤–æ–¥–∏–º—ã–µ –Ω–∏–∂–µ –¥–∞–Ω–Ω—ã–µ —É—Ç–≤–µ—Ä–∂–¥–∞—Ç—å, 
# —á—Ç–æ –Ω–∏—Ñ–µ–¥–∏–ø–∏–Ω –≤–ª–∏—è–µ—Ç –Ω–∞ –¥–∏–∞–º–µ—Ç—Ä –∫–æ—Ä–æ–Ω–∞—Ä–Ω—ã—Ö –∞—Ä—Ç–µ—Ä–∏–π?
#         1. –í—ã–ø–æ–ª–Ω–∏—Ç—å —Ä–∞—Å—á–µ—Ç—ã –≤ Python
#         2. –°–¥–µ–ª–∞–π—Ç–µ —Ä–∞—Å—á–µ—Ç –≤ —Ä—É—á–Ω—É—é
#         3. –°—Ä–∞–≤–Ω–∏—Ç–µ –∫—Ä–∏—Ç–µ—Ä–∏–π –°—Ç—å—é–¥–µ–Ω—Ç–∞ –∏ p-value —Å–æ –∑–Ω–∞—á–µ–Ω–∏—è–º–∏, –ø–æ–ª—É—á–µ–Ω–Ω—ã–º–∏ –≤ Python
#         x =np.array([2.5, 2.2, 2.6, 2, 2.1, 1.8,2.4, 2.3, 2.7, 2.7, 1.9])
#         y= np.array([2.5, 1.7, 1.5, 2.5, 1.4, 1.9, 2.3, 2.0, 2.6, 2.3, 2.2])

# 1. –í—ã–ø–æ–ª–Ω–∏—Ç—å —Ä–∞—Å—á–µ—Ç—ã –≤ Python
x = np.array([2.5, 2.2, 2.6, 2, 2.1, 1.8, 2.4, 2.3, 2.7, 2.7, 1.9])
y = np.array([2.5, 1.7, 1.5, 2.5, 1.4, 1.9, 2.3, 2.0, 2.6, 2.3, 2.2])

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Ç–µ—Å—Ç–∞ –°—Ç—å—é–¥–µ–Ω—Ç–∞
# –¥–≤—É—Ö–≤—ã–±–æ—Ä–æ—á–Ω—ã–π —Ç–µ—Å—Ç —Å –Ω–µ–∑–∞–≤–∏—Å–∏–º—ã–º–∏ –≤—ã–±–æ—Ä–∫–∞–º–∏ 
print()
print(stats.ttest_ind(x, y)) # Ttest_indResult(statistic=1.3283484757831463, pvalue=0.19902265798859645)
# statistic=1.3283484757831463 —ç—Ç–æ t–Ω - –Ω–∞–±–ª—é–¥–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ, –∫–æ—Ç–æ—Ä–æ–µ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç—Å—è —Å —Ç–∞–±–ª–∏—á–Ω—ã–º t—Ç
# pvalue=0.19902265798859645 —Å—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç—Å—è —Å –∞–ª—å—Ñ–∞ (—Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–æ–π –∑–Ω–∞—á–∏–º–æ—Å—Ç—å—é, –∫–æ—Ç–æ—Ä—É—é –≤—ã–±–∏—Ä–∞–µ–º —Å–∞–º–∏, –∞–ª—å—Ñ–∞ = 5%)
# pvalue=0.1990 > –∞–ª—å—Ñ–∞=0.05 => 0-—è –≥–∏–ø–æ—Ç–µ–∑–∞ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–∞–µ—Ç—Å—è - —Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∏ –∑–Ω–∞—á–∏–º—ã—Ö —Ä–∞–∑–ª–∏—á–∏–π –º–µ–∂–¥—É –Ω–∏—Ñ–µ–¥–∏–ø–∏–Ω–æ–º –∏ –ø–ª–∞—Ü–µ–±–æ –Ω–µ—Ç.
# –≠—Ç–æ –∑–Ω–∞—á–∏—Ç, —á—Ç–æ –Ω–∏—Ñ–µ–¥–∏–ø–∏–Ω –Ω–µ –≤–ª–∏—è–µ—Ç –Ω–∞ –¥–∏–∞–º–µ—Ç—Ä –∫–æ—Ä–æ–Ω–∞—Ä–Ω—ã—Ö –∞—Ä—Ç–µ—Ä–∏–π


# 2. –°–¥–µ–ª–∞–π—Ç–µ —Ä–∞—Å—á–µ—Ç –≤ —Ä—É—á–Ω—É—é
# ùë°–Ω=(ùëã¬†ÃÖ‚àíŒº)/(ùûº–Ω/‚àöùëõ)
