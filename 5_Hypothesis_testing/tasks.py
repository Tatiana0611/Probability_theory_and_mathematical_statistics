print(t) # 1.3283484

# –ü–æ —Ç–∞–±–ª–∏—Ü–µ –Ω–∞—Ö–æ–¥–∏–º —á–∏—Å–ª–æ —Å—Ç–µ–ø–µ–Ω–µ–π —Å–≤–æ–±–æ–¥—ã, —Ä–∞–≤–Ω–æ–µ –æ–±—ä–µ–º—É –∫–∞–∂–¥–æ–π –≤—ã–±–æ—Ä–∫–∏ - 1, —Ç.–µ. (11 - 1) + (11 - 1) = 20
